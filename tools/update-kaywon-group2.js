async function run() {
  let success = 0
  let fail = 0

  for (const item of data) {
    const { data: existing, error: fetchErr } = await supabase
      .from('universities')
      .select('id')
      .eq('name', item.name)
      .eq('department', item.department)
      .single()

    if (fetchErr || !existing) {
      console.error(`❌ 조회 실패: ${item.name} / ${item.department}`)
      fail++
      continue
    }

    const { error: updateErr } = await supabase
      .from('universities')
      .update({ practice_guide: item.guide })
      .eq('id', existing.id)

    if (updateErr) {
      console.error(`❌ 업데이트 실패: ${item.name} / ${item.department}: ${updateErr.message}`)
      fail++
    } else {
      console.log(`✅ ${item.department}`)
      success++
    }
  }

  console.log(`\n완료: 성공 ${success}개, 실패 ${fail}개`)
}

