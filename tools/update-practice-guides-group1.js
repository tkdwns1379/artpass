async function run() {
  let success = 0
  let fail = 0

  for (const item of data) {
    const { error } = await supabase
      .from('universities')
      .update({ practice_guide: item.guide })
      .eq('name', item.name)
      .eq('department', item.department)

    if (error) {
      console.error(`❌ ${item.name} / ${item.department}: ${error.message}`)
      fail++
    } else {
      console.log(`✅ ${item.name} / ${item.department}`)
      success++
    }
  }

  console.log(`\n완료: 성공 ${success}개, 실패 ${fail}개`)
}

run()
