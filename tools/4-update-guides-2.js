async function main() {
  const loginRes = await req({
    ...BASE, path: '/api/auth/login', method: 'POST',
    headers: { 'Content-Type': 'application/json' }
  }, { email: 'admin@artpass.kr', password: 'artpass1234' });

  if (!loginRes.token) { console.error('로그인 실패:', loginRes); return; }
  const token = loginRes.token;
  console.log('✅ 로그인 완료\n');

  const unis = await req({ ...BASE, path: '/api/universities', method: 'GET' });
  console.log(`대학 목록: ${unis.length}개\n`);

  let updated = 0, skipped = 0, notFound = 0;

  for (const [key, guide] of Object.entries(GUIDES)) {
    const [name, department] = key.split('||');
    const uni = unis.find(u => u.name === name && u.department === department);

    if (!uni) {
      console.log(`⚠️  없음: ${name} | ${department}`);
      notFound++;
      continue;
    }

    if (uni.tips && uni.tips.length > 0) {
      console.log(`⏭️  이미 있음: ${name} | ${department}`);
      skipped++;
      continue;
    }

    const res = await req({
      ...BASE, path: `/api/universities/${uni.id}`, method: 'PUT',
      headers: { 'Content-Type': 'application/json', 'Authorization': `Bearer ${token}` }
    }, guide);

    if (res && res.id) {
      console.log(`✅ 업데이트: ${name} | ${department}`);
      updated++;
    } else {
      console.log(`❌ 실패: ${name} | ${department}`);
    }
  }

  console.log(`\n========================================`);
  console.log(`업데이트: ${updated}개 | 이미 있음: ${skipped}개 | 없음: ${notFound}개`);
  console.log(`========================================`);
}

main().catch(console.error);
