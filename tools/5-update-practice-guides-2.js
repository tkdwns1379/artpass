async function login() {
  const data = await req({
    ...BASE,
    path: '/api/auth/login',
    method: 'POST',
    headers: { 'Content-Type': 'application/json' }
  }, { email: 'admin@artpass.kr', password: 'artpass1234' });
  if (!data.token) throw new Error('로그인 실패: ' + JSON.stringify(data));
  console.log('✅ 로그인 성공');
  return data.token;
}

async function getAllUniversities(token) {
  const data = await req({
    ...BASE,
    path: '/api/universities',
    method: 'GET',
    headers: { 'Authorization': 'Bearer ' + token }
  });
  return Array.isArray(data) ? data : (data.data || data.universities || []);
}

async function updateUniversity(token, id, body) {
  return req({
    ...BASE,
    path: `/api/universities/${id}`,
    method: 'PUT',
    headers: {
      'Content-Type': 'application/json',
      'Authorization': 'Bearer ' + token
    }
  }, body);
}

async function main() {
  const token = await login();
  const universities = await getAllUniversities(token);
  console.log(`📚 전체 대학 수: ${universities.length}`);

  let success = 0, fail = 0, skip = 0;

  for (const [key, guideData] of Object.entries(PRACTICE_GUIDES)) {
    const [univName, deptName] = key.split('||');

    const found = universities.find(u => {
      const uName = (u.universityName || u.name || '').trim();
      const depts = u.departments || [];
      const deptMatch = depts.find(d => {
        const dName = (d.name || d.departmentName || '').trim();
        return dName === deptName.trim();
      });
      return uName === univName.trim() && deptMatch;
    });

    if (!found) {
      console.log(`⚠️  [미발견] ${univName} | ${deptName}`);
      skip++;
      continue;
    }

    const dept = (found.departments || []).find(d => {
      const dName = (d.name || d.departmentName || '').trim();
      return dName === deptName.trim();
    });

    if (!dept) {
      console.log(`⚠️  [학과없음] ${univName} | ${deptName}`);
      skip++;
      continue;
    }

    if (dept.practiceGuide && Object.keys(dept.practiceGuide).length > 0) {
      console.log(`⏭️  [이미있음] ${univName} | ${deptName}`);
      skip++;
      continue;
    }

    const updatedDept = { ...dept, ...guideData };
    const updatedDepartments = (found.departments || []).map(d => {
      const dName = (d.name || d.departmentName || '').trim();
      return dName === deptName.trim() ? updatedDept : d;
    });

    const updateBody = { ...found, departments: updatedDepartments };

    try {
      const res = await updateUniversity(token, found._id || found.id, updateBody);
      if (res && (res._id || res.id || res.success)) {
        console.log(`✅ [업데이트] ${univName} | ${deptName}`);
        success++;
      } else {
        console.log(`❌ [실패] ${univName} | ${deptName}:`, JSON.stringify(res).slice(0, 100));
        fail++;
      }
    } catch (e) {
      console.log(`❌ [에러] ${univName} | ${deptName}:`, e.message);
      fail++;
    }
  }

  console.log(`\n🎉 완료 - 성공: ${success}, 실패: ${fail}, 건너뜀: ${skip}`);
}

main().catch(console.error);
